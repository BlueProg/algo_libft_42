#include "libft.h"
#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		if (n == -2147483648)
			write(fd, "-2147483648", 11);
		else
		{
			write(fd, "-", 1);
			ft_putnbr(-n);
		}
	}
	else
	{
		if (n > 10)
		{
			ft_putnbr(n / 10);
			write(fd, "0" + n % 10, 1);
		}
		write(fd, "0" + n, 1);
	}
}
