#include "libft.h"
#include <unistd.h>

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-n);
	}
	else
	{
		if (n > 10)
		{
			ft_putnbr(n / 10);
			write(1, "0" + n % 10, 1);
		}
		write(1, "0" + n, 1);
	}
}
